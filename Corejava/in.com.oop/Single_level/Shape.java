package Single_level;

public class Shape {
       
	String color;
	int BorderWidth;
	
	

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getBorderWidth() {
		return BorderWidth;
	}

	public void setBorderWidth(int borderWidth) {
		BorderWidth = borderWidth;
	}
}
