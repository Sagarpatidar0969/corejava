package in.co.batch;

public class Typecast {
	public static void main(String[] args) {
		
		short  a =8;
		double b = a; 
		System.out.println(b);
		
		double c = 4.0;
		short d = (short) c;
		System.out.println(d);
		
		int e = 55;
		double f = e;
		System.out.println(f);
		
		double g = 56;
		int h   = (int) g;
		System.out.println(h);
		
		float i = 77;
		double j = i;
		System.out.println(j);
		
		double k = 77;
		float l = (float) k;
		System.out.println(l);
		
		
		
		
	}
}